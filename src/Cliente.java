
public class Cliente {
	
	private String nome, codigoCartao, cpf, istituicaoDeEnsino, filiacao, profissao;
		
	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getCodigoCartao() {
		return codigoCartao;
	}

	public void setCodigoCartao(String codigoCartao) {
		this.codigoCartao = codigoCartao;
	}

	public String getCpf() {
		return cpf;
	}

	public void setCpf(String cpf) {
		this.cpf = cpf;
	}

	public String getIstituicaoDeEnsino() {
		return istituicaoDeEnsino;
	}

	public void setIstituicaoDeEnsino(String istituicaoDeEnsino) {
		this.istituicaoDeEnsino = istituicaoDeEnsino;
	}

	public String getFiliacao() {
		return this.filiacao;
	}

	public void setFiliacao(String filiacao) {
		this.filiacao = filiacao;
	}

	public String getProfissao() {
		return profissao;
	}

	public void setProfissao(String profissao) {
		this.profissao = profissao;
	}	
}
